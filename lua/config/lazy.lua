    { import = "lazyvim.plugins.extras.lang.tailwind" },
    { import = "lazyvim.plugins.extras.util.mini-hipatterns" },
    { import = "plugins" },
  },
  defaults = {
    lazy = false,
    version = false,
  },
  change_detection = {
    enabled = true,
    notify = false,
  },
  checker = { enabled = true, notify = false },
  performance = {
